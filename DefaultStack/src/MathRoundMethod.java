
public class MathRoundMethod {

	public static void main(String[] args) {
		double x = 2.5;
        long roundedX = Math.round(x);
        System.out.println("The rounded value of " + x + " is " + roundedX);

        float y = 2.5f;
        int roundedY = Math.round(y);
        System.out.println("The rounded value of " + y + " is " + roundedY);

	}

}
