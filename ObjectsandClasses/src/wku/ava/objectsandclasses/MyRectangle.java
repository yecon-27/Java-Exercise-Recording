public class MyRectangle {
	//when it is default: create a package
	double length;
	double width;
	
	public void area() {
		System.out.println("Area of a rectangle is: " + length * width);
	}
	public void perimeter() {
		System.out.println("Area of a perimeter is: " + 2 * (length + width));
	}
}

