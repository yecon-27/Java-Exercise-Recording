public class MAX_VALUE_and_MIN_VALUE {

	public static void main(String[] args) {
		System.out.println("The maximum integer is: " + Integer.MAX_VALUE);//2147483647
		System.out.println("The minimum positive float is: " + Float.MIN_VALUE);//1.4E-45
		System.out.println("The maximum double-precision floating-point number is: " + Double.MAX_VALUE);//1.7976931348623157E308
		System.out.println("The maximum short is: " + Short.MAX_VALUE);//32767
		System.out.println("The minimum short is: " + Short.MIN_VALUE);//-32768
		System.out.println("The maximum byte is: " + Byte.MAX_VALUE);//127
		System.out.println("The minimum integer is: " + Byte.MIN_VALUE);//-128

	}

}
