public class TypeValue_Method {

	public static void main(String[] args) {
		int DoubleObjectToint = new Double(12.4).intValue();
		System.out.println(DoubleObjectToint);
		
		double IntegerObjectTodouble = new Integer(12).doubleValue();
		System.out.println(IntegerObjectTodouble);
		
		float IntegerObjectTofloat = new Integer(625).floatValue();
		System.out.println(IntegerObjectTofloat);
		
		byte IntegerObjectTobyte = new Integer(32).byteValue();
		System.out.println(IntegerObjectTobyte);
		
	}

}
