public class ValueOF_Method {
//converted string can be used in arithmetic operation
	public static void main(String[] args) {
		Double doubleObject = Double.valueOf("12.4");
		System.out.println(doubleObject + 7);
		
		Double integerObject = Double.valueOf("12");
		System.out.println(integerObject + 5);

	}

}
