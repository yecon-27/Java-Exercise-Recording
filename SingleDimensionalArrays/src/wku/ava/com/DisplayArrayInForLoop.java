public class DisplayArrayInForLoop {
	public static void main(String[] args) {
		String[] cars = { "Volvo" , "BMW", "Ford"};
		//Use foreach loop to walk through each element in cars array
		for(String i : cars) {
			System.out.println(i);
		}//end of the foreach loop
	}//end of the main
}//end of the class
