public class DisplayArrayInForeachLoop {
	public static void main(String[] args) {
		String[] cars = { "Volvo" , "BMW", "Ford"};
		//Use for loop to display the cars array
		for(int i = 0 ; i < cars.length ; i++) {
			System.out.println(cars[i]);
		}//end of the for loop
	}//end of the main
}//end of the class
