public class SingleDimensionalArrayInitializer {
	public static void main(String[] args) {
		//Create an array of type String called cars, using Array initializers:
		/*elementType[] arrayRefVar = {value 0, value 1, ..., value k};*/
		String[] cars = {"Volvo", "BMW", "Ford"};
		//display the array cars:
		for(int i = 0; i < cars.length; i++) {
			System.out.print(cars[i] + " ");
		}//end of the for loop
	}//end of the main
}// end of the class


